cd_lines = CD_DATA.strip().split("\n")
cd_anchors = []
for line in cd_lines:
    ym, val = line.split(",")
    cd_anchors.append((pd.to_datetime(f"{ym}-01"), float(val) / 100.0))  # 비율로 변환

# 일별로 보간 (선형)
all_dates = pd.date_range("2000-01-01", "2027-12-31", freq="D")
df_cd = pd.DataFrame({"Date": all_dates})
df_cd["Rate"] = float("nan")

# anchor 날짜에 값 박기
for d, v in cd_anchors:
    df_cd.loc[df_cd["Date"] == d, "Rate"] = v

# 선형 보간
df_cd["Rate"] = df_cd["Rate"].interpolate(method="linear")
df_cd["Rate"] = df_cd["Rate"].ffill().bfill()
df_cd["Date"] = df_cd["Date"].dt.strftime("%Y-%m-%d")
out = os.path.join(OUT_DIR, "kr_cd91.csv")
df_cd.to_csv(out, index=False, encoding="utf-8")
print(f"  saved {len(df_cd)} rows -> {out}")

